function shuffleArray(array) {
    return array.sort(() => Math.random() - 0.5);
}

const shuffledImages = shuffleArray([...images]);

function renderPlaylist(songs) {
    const playlist = document.getElementById('playlist');
    playlist.innerHTML = '';
    songs.forEach((song, index) => {
        const songItem = document.createElement('div');
        songItem.className = 'song-item';
        songItem.innerHTML = `
            <img src="${shuffledImages[index % shuffledImages.length]}" alt="Christmas" class="song-image">
            <div class="song-info">${song.title}</div>
            <button class="play-btn">
                <div class="play-icon"></div>
            </button>
        `;
         songItem.addEventListener('click', () => {
            localStorage.setItem('currentSong', JSON.stringify(song));
            localStorage.setItem('currentSongIndex', index);
            localStorage.setItem('currentPlaylist', JSON.stringify(songs));
            
        window.location.href = 'lyrics.html';
    });
        playlist.appendChild(songItem);
    });
    document.getElementById('songCount').textContent = `${songs.length} Songs`;
}

